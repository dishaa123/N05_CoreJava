package org.tnsif.wrraperclassdemo;
/* program to demonstrate on autoboxing*/
/*AutoBoxing :  Conversion of primitive type to object type*/
public class AutoBoxing {

	public static void main(String[] args) {
		char c ='A';
		//converting primitive Data type into object type
		Character ch = c;
		System.out.println(ch);
	
	}

}
