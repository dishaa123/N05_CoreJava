/* program to demonstrate on UnBoxing*/
/* UnBoxing : Conversion of object to primitive type*/
public class UnBoxing {

	public static void main(String[] args) {
		Integer a = 23;
		// converting object type to primitive type
		int a1 = a;
		System.out.println(a1);

	}

}
